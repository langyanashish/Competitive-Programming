#include<bits/stdc++.h>
using namespace std;
int main()
{
	int t;
	cin>>t;
	while(t--)
	{
		int n;
		cin>>n;
		int a[n];
		for(int i=0;i<n;i++)	cin>>a[i];
		int k;
		cin>>k;
		int x=a[k-1];
		sort(a,a+n);
		for(int i=0;i<n;i++)
			if(a[i]==x)
			{
				x=i;
				break;
			}
		cout<<x+1<<endl;
	}
	return 0;
}

